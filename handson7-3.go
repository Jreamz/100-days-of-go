package main

import "fmt"

func main() {
	nOne, nTwo, nThree := 747, 911, 90210
	fmt.Printf("Value: %v \t binary: %b \t hexadecimal: %#X \t decimal: %d \n", nOne, nOne, nOne, nOne)
	fmt.Printf("Value: %v \t binary: %b \t hexadecimal: %#X \t decimal: %d \n", nTwo, nTwo, nTwo, nTwo)
	fmt.Printf("Value: %v \t binary: %b \t hexadecimal: %#X \t decimal: %d \n", nThree, nThree, nThree, nThree)

}

